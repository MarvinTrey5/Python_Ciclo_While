# Hacer un ejercicio en Python
# que mediante un ciclo while sume 
# Únicamente los números impares del rango 1 al 20.

print("-----------------------")
print("Se presentan los números impares del rango")
suma=0
i=1
while i<=20:
      print(i," ",end=" ")
      suma=suma+i
      i=i+2
print("")
print("------------------------")
print("El total de la suma de los números impares es: ",suma)

print("Fin del ejercicio")
